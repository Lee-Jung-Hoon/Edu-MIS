package kr.co.edumis.user.member.vo;

public class MemberVO {
	private String No;
	private String Id;
	private String Pass;
	private String Name;
	private String Zcode;
	private String Zaddr;
	private String Email;
	private String Birth;
	private String Phone;
	private String Major;
	private String JoinDate;
	private String DropCheck;
	private String DropDate;
	private String DropReason;
	private String Group;
	
	public String getNo() {
		return No;
	}
	public void setNo(String no) {
		No = no;
	}
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getPass() {
		return Pass;
	}
	public void setPass(String pass) {
		Pass = pass;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getZcode() {
		return Zcode;
	}
	public void setZcode(String zcode) {
		Zcode = zcode;
	}
	public String getZaddr() {
		return Zaddr;
	}
	public void setZaddr(String zaddr) {
		Zaddr = zaddr;
	}
	public String getEmail() {
		return Email;
	}
	public void setEmail(String email) {
		Email = email;
	}
	public String getBirth() {
		return Birth;
	}
	public void setBirth(String birth) {
		Birth = birth;
	}
	public String getPhone() {
		return Phone;
	}
	public void setPhone(String phone) {
		Phone = phone;
	}
	public String getMajor() {
		return Major;
	}
	public void setMajor(String major) {
		Major = major;
	}
	public String getJoinDate() {
		return JoinDate;
	}
	public void setJoinDate(String joinDate) {
		JoinDate = joinDate;
	}
	public String getDropCheck() {
		return DropCheck;
	}
	public void setDropCheck(String dropCheck) {
		DropCheck = dropCheck;
	}
	public String getDropDate() {
		return DropDate;
	}
	public void setDropDate(String dropDate) {
		DropDate = dropDate;
	}
	public String getDropReason() {
		return DropReason;
	}
	public void setDropReason(String dropReason) {
		DropReason = dropReason;
	}
	public String getGroup() {
		return Group;
	}
	public void setGroup(String group) {
		Group = group;
	}	
}
